#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*ret;

	ret = (char *)malloc(ft_strlen(s1) + 1);
	if (!ret)
		return (0);
	i = 0;
	while (s1[i])
	{
		ret[i] = s1[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
